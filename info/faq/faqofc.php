<h4>Regular maintenance of a Windows PC</h4>
<ol>
    <li>Uninstall Norton/McAfee Anti-Virus that was installed by Windows, if not updated.  Restart if required</li>
    <li>Uninstall other unnecessary apps.</li>
    <li>Delete unnecessary and obsolete files, pictures, downloads in Documents/Pictures/Downloads</li>
    <li>Delete viber images that were automatically downloaded <br>
        C:\Users\<i>username</i>\Documents\ViberDownloads <br>
        C:\Users\<i>username</i>\Documents\ViberDownloads\PublicAccountMedia<br>
        C:\Users\<i>username</i>\Documents\ViberDownloads\Temporary
    </li>
    <li>Empty Recycle Bin</li>
    <li>Run Disk Cleanup (Start/Windows Administrative Tools/Disk Cleanup)</li>
    <li>Run Disk Defragmenter (Start/Windows Administrative Tools/Defragment and Optimize Files)</li>
    <li>Restart</li>
</ol>

