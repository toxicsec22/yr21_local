<p>
<strong>1.	&nbsp; &nbsp; REDUCING TRANSMISSION OF COVID-19</strong>
</p>
<p style="margin-left:24pt;margin-top:-8pt;">Every person shall:</p>
<p style="margin-left:24pt;">
1.1	&nbsp;	Wear face masks and goggles/shades at all times and remove the same only when eating/drinking. Should cloth masks be used, the washable type shall be worn but additional filter material such as tissue papers inside the masks may be added.
</p>
<p style="margin-left:24pt;">
1.2	&nbsp;	Record/log body temperature daily.
</p>
<p style="margin-left:24pt;">
1.3	&nbsp;	Accomplish Daily Health Check Questionnaire.
</p>
<p style="margin-left:47pt;">
Person with temperature > 37.5&#xb0;C, even after a 5 minute rest, or if their response in the questionnaire needs further evaluation by the assigned safety officer, the person  shall be isolated in an area identified by the company and not allowed to enter the company common area. The isolation area should be well ventilated and must be disinfected frequently.
</p>
<p style="margin-left:47pt;">
Employee’s showing any of the following symptoms: Fever and/or Cough, Colds, Sore Throat, Muscle and Joint Pains, Fatigue, Difficulty of Breathing, Shortness of Breath and Diarrhea shall be advised to go home or seek for medical attention.
</p>
<p style="margin-left:24pt;">
1.4	&nbsp; Proper Sneezing/Coughing Practices.
</p>
<p style="margin-left:47pt;">
Cover the mouth at all times when coughing or sneezing to prevent viral transmission through respiratory droplets. Use a clean tissue and dispose them properly. In the absence of a cover, shirt sleeves may use, but NOT the hands.
</p>
<p style="margin-left:24pt;">
1.5	&nbsp; Proper Handwashing Technique
</p>
<p style="margin-left:47pt;">
COVID-19 Viruses may stay on surfaces, tables and doorknobs for 3 hours or more. To prevent transmission through contact, please perform proper and thorough handwashing with soap and water for at least 30 seconds. Do this as frequently as possible, most especially before and after eating. Use at least 70% alcohol or hand sanitizers if hand washing facility is not available.
</p>
<p style="margin-left:24pt;">	
1.6	&nbsp;	Observe Physical or Social Distancing by keeping a 1-meter distance to the next person. 
</p>
<p style="margin-left:24pt;">
1.7	&nbsp;	Employees are advised to frequently clean and disinfect their work stations, cell phones and gadgets. If possible, place cellphones in a zip lock bag for ease of cleaning. Sharing of tables/space, utensils and office essentials such as pens are also discouraged.
</p>
<p style="margin-left:24pt;">
1.8	&nbsp;	Always keep right or have unidirectional movements along the aisles, corridors and walkways.
</p>
<p style="margin-left:24pt;">
1.9	&nbsp;	Perform  everyday actions to stay healthy such as:
</p>
<p style="margin-left:47pt;">
a.	&nbsp;	Eat nutritious and well-cooked food;<br>
b.	&nbsp;	Drink plenty of fluids and avoid alcoholic beverages;<br>
c.	&nbsp;	Increase the body’s resistance by having adequate rest and at least eight (8) hours of sleep; <br>
d.	&nbsp;	Exercise regularly;<br>
e.	&nbsp;	Take vitamins;<br>
f.	&nbsp;	&nbsp;When needing counselling or presenting mental health concerns, call the attention of proper personnel for the proper guidance.<br>
</p>
<p>
<strong>2.	&nbsp; &nbsp; WORK MODIFICATION FOR HIGH RISK INDIVIDUALS</strong>
</p>

<p style="margin-left:47pt;">
Person who are Sixty (60) years old and above and those who have pre-existing medical conditions are classified by WHO / DOH as vulnerable and high risk groups.
</p>
<p style="margin-left:47pt;">
•	&nbsp;	People with chronic lung disease or moderate to severe asthma<br>
•	&nbsp;	People who have heart conditions<br>
•	&nbsp;	People with diabetes<br>
•	&nbsp;	People with liver disease<br>
•	&nbsp;	Pregnant woman<br>
</p>
<p style="margin-left:24pt;">
2.1	Employee’s with Pre-existing Medical Conditions;
</p>
<p style="margin-left:47pt;">
a.	&nbsp;	Work From Home (WFH) arrangement is highly recommended.<br>
b.	&nbsp;	If Work From Home arrangement cannot be fully implemented:<br>
&nbsp; &nbsp; &nbsp; •	Physical reporting to the office has to be limited to three (3) days per week.
</p>
<p>
<strong>3.	&nbsp; &nbsp; GUESTS AND VISITORS ACCEPTANCE PROTOCOL</strong>
</p>
<p style="margin-left:24pt;">
3.1	&nbsp; For applicant interviews, the use of video conferencing and other similar technologies is highly encouraged.
</p>
<p style="margin-left:24pt;">
3.2	&nbsp; However, if the visit / appointment is of utmost importance, the Visitor / Guest Applicant shall be required to fill up<br> Visitor Health Checklist.
</p>
<p style="margin-left:24pt;">
3.3	&nbsp; Allow only one visitor/collector at the time at the reception or selling area.
</p>
<p>
<strong>4.	&nbsp; &nbsp; PANTRIES</strong>
</p>
<p style="margin-left:24pt;">
4.1	&nbsp;	Utensils, Appliances and furniture shall be disinfected prior to use.
</p>
<p style="margin-left:24pt;">
4.2	&nbsp;	Staggered break time schedule shall be implemented to reduce congestion.
</p>
<p style="margin-left:24pt;">
4.3	&nbsp;	Bringing own food and utensils is recommended.
</p>
<p>
<strong>5.	&nbsp; &nbsp; DOCUMENT / PARCEL TRANSMISSION & RECEIVING PROTOCOL</strong>
</p>
<p style="margin-left:24pt;">
5.1	&nbsp; All Documents / Parcels shall be wrapped in plastic bags prior to transmission.
</p>
<p style="margin-left:24pt;">
5.2	&nbsp; The receiver shall disinfect the plastic bags (zip bags) prior to opening of the documents/parcel. The used zip bags/ plastics shall be disposed in the designated Hazardous Waste Bins.
</p>
<p style="margin-left:24pt;">
5.3	&nbsp; The receiver shall wear goggles and face mask during the opening of the documents.
</p>
<p style="margin-left:24pt;">
5.4	&nbsp; Storing and opening the document after a period of twenty-four (24) hours can be an option.
</p>
<p>
<strong>6.	&nbsp; &nbsp; FACILITIES MAINTENANCE PROTOCOLS</strong>
</p>
<p style="margin-left:24pt;">
6.1	&nbsp;Tables and chairs shall be re-arranged to facilitate implementation of social distancing measures in all company premises, including offices, meeting rooms, training rooms, pantries, warehouses, staff house and waiting areas.
</p>
<p style="margin-left:24pt;">
6.2	&nbsp; Foot baths shall be installed in all facility entrances / access to disinfect shoe soles that may carry the virus.
</p>
<p style="margin-left:24pt;">
6.3	&nbsp;Thorough cleaning and disinfection of frequently touched and accessed areas including top of the front store cabinet, tables, doorknobs, door access, light switches, handles, desks, toilets, faucets and sinks on an hourly basis.
</p>
<p style="margin-left:24pt;">
6.4	&nbsp; General cleaning and disinfection shall be implemented daily after office hours for frequently visited areas like Hallways, Meeting rooms, Pantries, Warehouses, and Waiting Areas.
</p>
<p style="margin-left:24pt;">
6.5	&nbsp; Twice a day disposal of trash shall be observed, and corresponding proper cleaning and disinfection to be carried out. 
</p>
<p style="margin-left:24pt;">
6.6	&nbsp; Alcohol dispensers and/or hand sanitizers shall be placed in strategic locations such as, but not limited to, entrance of premises and toilets. This shall be maintained and refilled regularly.
</p>
<p style="margin-left:24pt;">
6.7	&nbsp; Employees shall be required to wash hands before and after work.
</p>
<p style="margin-left:24pt;">
6.8	&nbsp; Daily cleaning and disinfection of construction equipment, store tools, office tools and supplies shall be implemented. Employees shall ensure that the equipment/tools are disinfected before and after use.
</p>
<p style="margin-left:24pt;">
6.9	&nbsp; Sharing of tools / equipment is highly discouraged. If sharing is necessary, the shared tools/equipment shall be sanitized prior to transferring to the next user.
</p>
<p style="margin-left:24pt;">
6.10	&nbsp; Store must have installed protective plastic cover in front of the store (selling area) for added protection.
</p>
<p>
<strong>7.	&nbsp; &nbsp; DISCOURAGED FROM COMING TO WORK</strong>
</p>	
<p style="margin-left:24pt;">
•	&nbsp; Not feeling well (flu like symptoms, cough, difficulty in breathing)<br>
•	&nbsp; When one of the companions in the house experiences flu-like symptoms.
</p>
<p>
<strong>8.	&nbsp; &nbsp; PUI OR POSITIVE FOR COVID</strong>
</p>

<p style="margin-left:24pt;">
In the event that a person is suspected as having COVID-19<br><br>

8.1	&nbsp;	The person shall immediately proceed to the isolation area designated in the workplace and never remove his/her mask;<br><br>

8.2	&nbsp;	Person attending to the suspected with covid-19 should wear appropriate PPEs. Company protocols for transport for suspect <br>COVID-19 cases and for PCR testing, should be in place including providing for ambulance conduction. Hospitals will report to the <br>DOH for COVID-19 suspect.<br><br>

8.3	&nbsp;	Decontamination of workplace
</p>
<p style="margin-left:47pt;">
i. Workplace shall be decontaminated with appropriate disinfectant (e.g. chlorine bleaching solution and 1:100 phenol based disinfectant); 
<br><br>
ii.	After decontamination of the work area, work can resume after 24 hours; and
<br><br>
iii. Every person present in the work area with the suspect COVID-19 person shall go on 14 days home quarantine with specific instructions from the safety officer in-charge on monitoring of symptoms and possible next steps. If a suspect COVID-19 person has a negative result, everybody may be allowed to report back to work. 
</p>
<p style="margin-left:24pt;">
8.4	In the event that a employee is sick or has fever but is not suspected to have COVID-19 (ex., urinary infection, wound infection or any diseases not related to lungs or respiratory tract) the employer must advise the employee to take prudent measures to limit the spread of communicable diseases, as follows:
</p>
<p style="margin-left:24pt;">
i.	Stay at home and keep away from work or crowds;<br>
ii.	Take adequate rest and take plenty of fluids;<br>
iii.	Practice personal hygiene to prevent spread of disease; and<br>
iv.	Seek appropriate medical care if there is persistent fever, when difficulty of breathing has started, or when he/she becomes weak.<br>
</p>
<p style="margin-left:24pt;">
8.5	Contact Tracing within one meter of person for >=15 minutes within the last 7-14 days.
</p>
<p style="margin-left:24pt;">
• &nbsp; &nbsp; Person who is PUI/PUM shall report the list of persons he/she interacted with in his area. <br>
• &nbsp; Person who had interaction with the PUI/PUM person shall issue a detailed contact tracing/tracking: time of interaction, duration, possible physical contact, sharing of tangible files, distance of physical contact.<br>
• &nbsp; &nbsp; Report to the Barangay where the person resides<br>
• &nbsp; &nbsp; Counseling of the person under PUM<br>
• &nbsp; &nbsp; Inform immediate family member<br>
</p>


<p>
<strong>9.	&nbsp; &nbsp; DUTIES OF EMPLOYERS AND EMPLOYEES</strong>
</p>
 <p style="margin-left:24pt;margin-top:-8pt;">
Employers shall:</p>
<p style="margin-left:47pt;">
1.	&nbsp;	Provide the necessary company policies for the prevention and control of COVID-19 in consultation with employees. Advocacy and IEC programs should be taken from DOH, WHO and reliable sources of information on COVID-19;
</p>
<p style="margin-left:47pt;">
2.	&nbsp;Provide resources and materials needed to keep the employees healthy and the workplace safe, e.g., masks, soap, sanitizer, disinfectant, PPEs, etc;
</p>
<p style="margin-left:47pt;">
3.	&nbsp;Designate the safety officer to monitor COVID-19 prevention and control measures such as physical distancing, wearing of masks, regular disinfection, compliance to thermal scanning and accomplishing health symptom questionnaire;
</p>
<p style="margin-left:47pt;">
4.	&nbsp;	HMO benefit can use up to the maximum limit per year. For those who are not qualified for HMO benefit can avail financial assistance up to the maximum limit indicated in the company policy.
</p>
<p style="margin-left:47pt;">
5.	&nbsp;	Where feasible, provide shuttle services, bikes and/or allow stay-in in the branch to lessen travel and people movement;
</p>
<p style="margin-left:47pt;">
6.	&nbsp;	Enjoin the hiring from the local community; and
</p>
<p style="margin-left:47pt;">
7.	&nbsp;Put up a COVID-19 Hotline and Call Center for employees to report if symptomatic, and daily monitoring scheme of our “suspect” employee condition.
</p>
<p style="margin-left:47pt;">
8.	&nbsp;	Reporting of illness/diseases/injuries.
</p>
<p style="margin-left:60pt;">
The employer shall provide the DOLE through its Regional Office copy furnished DOH, monthly reporting of illness, diseases and injuries utilizing the DOLE Work Accident/Illness Report Form (WAIR)
</p>
<p style="margin-left:47pt;">
Every person shall:  
</p>
<p style="margin-left:60pt;">
Comply with all workplace measures in place for the prevention and control of COVID-19, such as, frequent hand washing, wearing of masks, observe the physical distancing always, etc.;
</p>
