<h4>Company Email</h4>

To keep your email running smoothly, ensure that your mailbox is not full.
<br><br>
<ol>
    <li>If you are using an app aside from the company webmail (https://1rotarytrading.com/webmail), you must know the difference between in storage whether pop3 or imap.</li>
